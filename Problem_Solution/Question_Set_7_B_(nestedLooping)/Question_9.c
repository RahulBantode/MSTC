#include <stdio.h>
#include <stdlib.h>

int main(void)
{
  int i, j;
  for (i = 1; i < 100; i <<= 2)
  {
    for (j = 4; j < 256; j <<= 3)
      printf("i = %d , j = %d\n", i, j);

    puts("--------------------------------------");
  }
  exit(0);
}

